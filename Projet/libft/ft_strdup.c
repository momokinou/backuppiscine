#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int i;
	char*dest;

	i = 0;
	while (src[i])
		i++;
	i++;
	dest = malloc(i * sizeof(int));
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
