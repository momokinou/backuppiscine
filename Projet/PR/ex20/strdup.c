#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;

	while (src[i])
		i++;
	i++;
	dest = malloc(i);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
