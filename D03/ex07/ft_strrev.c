char	*ft_strrev(char *str)
{
	int i;
	int j;
	int c;

	i = 0;
	j = 0;
	while(str[i])
	{
		i++;
	}
	i--;
	while(i > j)
	{
		c = str[j];
		str[j] = str[i];
		str[i] = c;
		i--;
		j++;
	}
	return(str);
}

