#include <stdio.h>

int		ft_atoi(char *str)
{
	int i;
	int nb;
	int neg;

	i = 0;
	nb = 0;
	neg = 1;
	while ((str[i] = 32) || (str[i] <= 9 && str[i] >= 13))
		i++;
	while (str[i] != '\0')
	{
		if (str[i] == '-')
		{
			neg = -1;
			i++;
		}
		else
		{
			nb = nb * 10 + (str[i] - '0');
			i++;
		}
	}
	return (nb * neg);
}
